package org.springhack.stickercrap;

public class Sticker {

    public String name;
    public String url;
    public String author;
    public String image;

    public Sticker(String n, String u, String a, String i) {
        this.name = n;
        this.url = u;
        this.author = a;
        this.image = i;
    }
}
